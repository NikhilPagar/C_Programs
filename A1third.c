#include<stdio.h>

 void Display()
 {
 	int i = 0;
 	int j = 5;

 	for(i=0;i<5;j--)
 	{
 		printf("%d ",j);
 		i++;
 	}
 }
int main()
{
	Display();

	return 0;
}